namespace num1a10
{
    internal class Program
    {
        static void Numeros()
        {
            Console.WriteLine("Imprimir Números del 1 al 10:");
            for (int numero = 1; numero <= 10; numero++) Console.WriteLine(numero);
        }
        static void Main(string[] args)
        {
            Numeros();
        }
    }
}
